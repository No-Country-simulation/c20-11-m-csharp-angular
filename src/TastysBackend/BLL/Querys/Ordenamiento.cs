namespace Tastys.BLL;

/// <summary>
/// Se usa para indicar el ordenamiento.
/// </summary>
public enum Ordenamiento
{
    /// <summary>
    /// No ordenar.
    /// </summary>
    Ninguno,
    /// <summary>
    /// Ordenar ascendentemente (de menor a mayor).
    /// </summary>
    Ascendente,
    /// <summary>
    /// Ordenar descendentemente (de mayor a menor).
    /// </summary>
    Descendente
}
